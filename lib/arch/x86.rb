    # Operand type
    OP_INVALID = 0   # Uninitialized.
    OP_REG     = 1   # Register operand.
    OP_IMM     = 2   # Immediate operand.
    OP_FP      = 3   # Floating-Point immediate operand.
    OP_MEM     = 4   # Memory operand

    class  MemoryOperand < FFI::Struct
      layout(
        :base, :uint,
        :index, :uint,
        :scale, :int,
        :disp, :int64
      )
    end

    class OperandValue < FFI::Union
      layout(
        :reg, :uint,
        :imm, :ulong_long,
        :fp, :double,
        :mem, MemoryOperand
      )
    end

    class Operand < FFI::Struct
      layout(
        :type, :uint,
        :value, OperandValue
      )

      # A spoonful of sugar...

      def value
        case self[:type]
        when OP_REG
          self[:value][:reg]
        when OP_IMM
          self[:value][:imm]
        when OP_MEM
          self[:value][:mem]
        when OP_FP
          self[:value][:fp]
        else
          nil
        end
      end

      def reg?
        self[:type] == OP_REG
      end

      def imm?
        self[:type] == OP_IMM
      end

      def mem?
        self[:type] == OP_MEM
      end

      def fp?
        self[:type] == OP_FP
      end

      def valid?
        [OP_MEM, OP_IMM, OP_FP, OP_REG].include? self[:type]
      end

    end

    class Instruction < FFI::Struct
      layout(
        :prefix, [:uint8, 5],
        :segment, :uint,
        :opcode, [:uint8, 3],
        :op_size, :uint8,
        :addr_size, :uint8,
        :disp_size, :uint8,
        :imm_size, :uint8,
        :modrm, :uint8,
        :sib, :uint8,
        :disp, :int32,
        :sib_index, :uint,
        :sib_scale, :int8,
        :sib_base, :uint,
        :op_count, :uint8,
        :operands, [Operand, 8]
      )
    end

